import React from 'react';
import { Line } from 'react-chartjs-2';
import {
  Chart as ChartJS,
  CategoryScale,
  LinearScale,
  PointElement,
  LineElement,
  Title,
  Tooltip,
  Legend,
} from 'chart.js';

ChartJS.register(
  CategoryScale,
  LinearScale,
  PointElement,
  LineElement,
  Title,
  Tooltip,
  Legend
);

const EpochChart = ({ epochs }) => {
  const data = {
    labels: epochs.map(epoch => epoch.epoch),
    datasets: [
      {
        label: 'Train Accuracy',
        data: epochs.map(epoch => epoch.train_acc),
        borderColor: 'rgba(75, 192, 192, 1)',
        backgroundColor: 'rgba(75, 192, 192, 0.2)',
        fill: false,
      },
      {
        label: 'Train Loss',
        data: epochs.map(epoch => epoch.train_loss),
        borderColor: 'rgba(255, 99, 132, 1)',
        backgroundColor: 'rgba(255, 99, 132, 0.2)',
        fill: false,
      },
      {
        label: 'Validation Accuracy',
        data: epochs.map(epoch => epoch.val_acc),
        borderColor: 'rgba(54, 162, 235, 1)',
        backgroundColor: 'rgba(54, 162, 235, 0.2)',
        fill: false,
      },
      {
        label: 'Validation Loss',
        data: epochs.map(epoch => epoch.val_loss),
        borderColor: 'rgba(153, 102, 255, 1)',
        backgroundColor: 'rgba(153, 102, 255, 0.2)',
        fill: false,
      }
    ]
  };

  const options = {
    responsive: true,
    plugins: {
      legend: {
        position: 'top',
      },
      title: {
        display: true,
        text: 'Training and Validation Metrics Over Epochs'
      }
    }
  };

  return <Line data={data} options={options} />;
};

export default EpochChart;
